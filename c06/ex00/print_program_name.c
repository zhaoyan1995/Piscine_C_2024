#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	c;

	if (argc > 0)
	{
		write(1, argv[0], 7);
		write(1, "\n", 1);
		c = argc + '0';
		write(1, &c, 1);
	}
	return (0);
}
